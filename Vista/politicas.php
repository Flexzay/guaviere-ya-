                            <h4>2. Recopilación de información personal</h4>
                            <p>Recopilamos información personal de nuestros usuarios cuando se registran en nuestra plataforma, realizan un pedido o se comunican con nosotros a través de nuestro servicio de atención al cliente. La información personal que recopilamos incluye, entre otras, el nombre, dirección, correo electrónico, número de teléfono, información de pago y dirección de entrega.
