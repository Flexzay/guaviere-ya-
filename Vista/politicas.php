                            <h4>1. Introducción</h4>
                            <p>En Guaviareya, nos tomamos en serio la privacidad de nuestros usuarios. Esta política de privacidad describe cómo recopilamos, utilizamos y protegemos la información personal que nuestros usuarios nos proporcionan a través de nuestra plataforma.
