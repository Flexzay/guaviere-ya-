                            <h4>3. Uso de información personal</h4>
                            <p>Nos comprometemos a proteger la información personal que recopilamos. Utilizamos medidas de seguridad adecuadas para proteger la información de nuestros usuarios contra pérdida, uso no autorizado o alteración.
                            </p>

                            <h4>4. Protección de la información personal</h4>
                            <p>No permitimos devoluciones ni reembolsos. Los usuarios pueden cancelar sus pedidos antes de que hayan sido procesados. Una vez procesado el pedido, no se podrán realizar cancelaciones.
                            </p>

                            <h4>5. Divulgación de información personal a terceros</h4>
                            <p>No vendemos ni alquilamos la información personal de nuestros usuarios a terceros. Sin embargo, podemos compartir información personal con empresas afiliadas o proveedores de servicios que nos ayudan a procesar pedidos y realizar entregas. También podemos compartir información personal en respuesta a órdenes judiciales, solicitudes gubernamentales o para proteger nuestros derechos legales.
                            </p>

