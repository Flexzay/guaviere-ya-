                            <p>Nos reservamos el derecho de modificar esta política de privacidad en cualquier momento. Si realizamos cambios significativos en la política, se lo notificaremos a nuestros usuarios a través de nuestra plataforma o por correo electrónico.
                            </p>

                            <h4>7. Derechos de los usuarios</h4>
                            <p>Los usuarios tienen derecho a acceder, corregir o eliminar su información personal en cualquier momento. Si un usuario desea ejercer cualquiera de estos derechos, puede ponerse en contacto con nuestro servicio de atención al cliente.
